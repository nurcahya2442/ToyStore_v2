package com.example.newlatihan.Model;

public class Toys {
    private int pic;
    private String tvJudul, tvDeks, tvHarga;

    public int getPic() {
        return pic;
    }

    public void setPic(int pic) {
        this.pic = pic;
    }

    public String getTvJudul() {
        return tvJudul;
    }

    public void setTvJudul(String tvJudul) {
        this.tvJudul = tvJudul;
    }

    public String getTvDeks() {
        return tvDeks;
    }

    public void setTvDeks(String tvDeks) {
        this.tvDeks = tvDeks;
    }

    public String getTvHarga() {
        return tvHarga;
    }

    public void setTvHarga(String tvHarga) {
        this.tvHarga = tvHarga;
    }
}
